    public static void slowType(String input, long speed) throws InterruptedException {
        char[] inputChars = input.toCharArray();

        for (int i = 0; i < inputChars.length; i++) {
            System.out.print(inputChars[i]); 
            Thread.sleep(speed);
        }
        System.out.print("\n");
    }

